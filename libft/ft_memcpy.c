#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t len)
{
	char		*d;
	const char	*s;

	d = dest;
	s = src;
	while ((dest || src) && len--)
		*d++ = *s++;
	return (dest);
}	
